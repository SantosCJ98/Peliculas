    private void btnSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSiguienteActionPerformed

        int res;

        if (validarFormulario()) {

            res = JOptionPane.showConfirmDialog(this, "¿Está seguro?", "¿Confirmar?", JOptionPane.YES_NO_OPTION);

            if (res == JOptionPane.YES_OPTION) {

                anyadirPeliculayCerrarVentanas();

                JOptionPane.showMessageDialog(this, "Confirmado", "Confirmado", JOptionPane.INFORMATION_MESSAGE);

            }

        }


    }//GEN-LAST:event_btnSiguienteActionPerformed

    /**
     * @param args the command line arguments
     */
    public void anyadirPeliculayCerrarVentanas() {

        String nombre = anyadir1.getTxtNombre();

        String genero = anyadir1.getGenero();

        Date fecha = dateFecha.getDate();

        String director = txtDirector.getText();

        String pais = (String)cmbPais.getSelectedItem();

        Pelicula pelicula = anyadir1.parte1(nombre, genero);

        pelicula.setDirector(director);

        pelicula.setFecha_estreno(fecha);

        pelicula.setNacionalidad(pais);

        anyadir1.lst.menu.ldn.anyadirPelicula(pelicula);

        dispose();

        anyadir1.lst.menu.refrescarContador();

        anyadir1.lst.inicializarTabla();

        anyadir1.dispose();

    }

    public boolean validarFormulario() {

        if (dateFecha.getDate() == null || !dateFecha.getDateFormatString().equals("d MMM y") || dateFecha.getDate().getYear() > new Date().getYear()) {

            JOptionPane.showMessageDialog(this, "La fecha no es válida", "ERROR", JOptionPane.ERROR_MESSAGE);

            return false;

        }

        if (txtDirector.getText().isEmpty() || txtDirector.getText() == null || !txtDirector.getText().matches("([A-ZÑÁÉÍÓÚ]([a-zñáéíóú]+)\\s?)+")) {

            JOptionPane.showMessageDialog(this, "El director no es válido", "ERROR", JOptionPane.ERROR_MESSAGE);

            return false;
        }

        return true;

    }

